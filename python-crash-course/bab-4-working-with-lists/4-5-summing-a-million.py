#Summing million 
#make a list 1 to million
millions = list(range(1,1000001))
print(millions)

#use min() and max() to make sure first and last number right
min_value = min(millions)
print(f"Max falue is {min_value}")
max_value = max(millions)
print(f"Max falue is {max_value}")
#use sum() to sum
sum_value = sum(millions)
print(f"Sum value is {sum_value}")